const ider = new Array ("toggle1","toggle2","toggle3","toggle4","toggle5")
let menuHTML = document.getElementsByClassName("product-card")[0].innerHTML

function endrekategori(id) {

    menu[0].classList.remove("active")
    menu[1].classList.remove("active")
    menu[2].classList.remove("active")
    menu[3].classList.remove("active")
    menu[4].classList.remove("active")
let knapp = document.getElementById(id)
    knapp.classList.add("active")

//https://stackoverflow.com/questions/4825295/onclick-to-get-the-id-of-the-clicked-button
//https://www.w3schools.com/jsref/jsref_indexof.asp


let index = ider.indexOf(id)

let knapptekst = resources[index]

menuHTML = ""
    menuHTML += (`${"<h3>"}${knapptekst.category}${"</h3>"}${"<p>"}${knapptekst.text}${"</p>"}${"<ul>"}`)  
    knapptekst.sources.map (link => {
     
    menuHTML +=  `${"<li>"} ${"<a href ="}${link.url}${">"}${link.title}${"</a>"}${ "</li>"}`
    })
    menuHTML += `${"</ul>"}`

    document.getElementsByClassName("product-card")[0].innerHTML = menuHTML
    
    
}             

